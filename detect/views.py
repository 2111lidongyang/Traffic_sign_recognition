def genrate():
    for i in (run()):
        ret, res_image = cv2.imencode('.jpeg', i)
        if ret:
            # 转换为byte类型的，存储在迭代器中
            yield (b'--frame\r\n'
                   b'Content-Type: image/jpeg\r\n\r\n' + res_image.tobytes() + b'\r\n')


def output(request):
    return StreamingHttpResponse(genrate(), content_type='multipart/x-mixed-replace; boundary=frame')
